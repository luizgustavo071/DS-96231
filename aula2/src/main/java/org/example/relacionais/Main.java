package org.example.relacionais;

public class Main {
    public static void main(String[] args) {
        // Declaração de variaveis
        int a = 10;
        int b = 22;
        int maior;

        //Operadores aritmeticos
        if (a > b) {
            maior = a;
        } else {
            maior = b;
        }

        //Exibindo resultados
        System.out.println("O maior numero é: " + maior);
    }
}
